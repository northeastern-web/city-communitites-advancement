    <h1>{!! $page->title !!}</h1>
    <p class="lead">{{ $page->description }} CCE matches student and staff groups with community organizations that are seeking volunteers to achieve their mission and goals. Our team can offer advice and support as you plan your next service project.</p>
    <h2>Get Started</h2>
    <p>Planning and organization are essential to a successful community service or volunteer project. Before considering where you’d like your group to serve, do a bit of brainstorming!</p>
    <ul>
        <li>What goals do you have for your group and will a service project help you reach those goals?</li>
        <li>How is your group looking to engage (direct service, philanthropy, ongoing partnerships)?</li>
        <li>What topic or issue are you hoping to address (education, environmental conservation, housing insecurity, etc.)?</li>
        <li>How many people do you expect to participate?</li>
        <li>When would you like your group to serve?</li>
    </ul>
    <p>Once you’ve answered these questions, it will be easier to find the right service opportunity for your group.</p>
    <h2>Create a Plan</h2>
    <p>Planning and completing a community service project requires time, effort, and commitment. Plan ahead and set realistic timelines and goals for your group. Attempting to go beyond your capabilities may result in challenges related to logistics, poor quality of service, and frustration from the community partner organization.</p>
    <h2>Choose a Project</h2>
    <p>Consider the service opportunities that exist in the surrounding community. What current programs or projects already exist that your group could support through volunteer engagement? Visit City and Community Engagement’s <a href="/service-finder">Service Finder</a> tool to search service opportunities that match your group’s interests, abilities, and skills. As you plan your service project, consider proximity (will you need transportation?), timing (avoid scheduling the activity on the same day as another big event or on a religious holiday), the size of your group (many nonprofits do not have the space or capacity to host large groups), and available resources.</p>
    <h2>Meet with CCE</h2>
    <p>CCE’s staff have the experience and resources to support your goal of engaging your group in community service. We can provide recommendations on how to approach your service project, which partners align best with your group’s interests, and best practices for implementing group volunteer projects. Email us at <a href="mailto:communityservice@northeastern.edu">communityservice@northeastern.edu</a>.</p>
    <h2>Plan Ahead </h2>
    <p>Agencies and organizations that rely on volunteers often need advance notice to host volunteer groups. It is imperative that you start planning early and understand your capacity. Don’t commit to a service project that your group cannot realistically complete; that will leave the partner organization without volunteer support. If you want to plan a service activity with a large number of volunteers (15+) at a specific time of year or with a specific community organization, we recommend you begin planning at least 3–4 months in advance.</p>
    <h2>Week of the Event</h2>
    <p>Confirm the following with the contact at the community organization:</p>
    <ul>
        <li>What time your group needs to be there</li>
        <li>What supplies your group needs to bring</li>
        <li>What service work your group will be doing</li>
        <li>What agency representative will be on-site the day of your event</li>
    </ul>
    <h2>Day of the Event</h2>
    <ul>
        <li>Arrive early</li>
        <li>Check in with the contact at the community organization</li>
        <li>Support your team throughout the service project</li>
        <li>Thank the partner for hosting your group and welcoming you into their space</li>
    </ul>
    <h2>Contact</h2>
    <p>For more information about group volunteer opportunities or to speak with a representative of the CCE team, please email <a href="mailto:communityservice@northeastern.edu">communityservice@northeastern.edu</a>.</p>
