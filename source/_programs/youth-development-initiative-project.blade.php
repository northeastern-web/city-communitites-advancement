        <h1>{{ $page->title }}</h1>
        <p class="lead">The Youth Development Initiative Project provides underserved youth in neighborhoods near Northeastern with tutoring, mentoring, and support services to prepare them for college and successful careers. YDIP founder Joseph Warren believed that both students and their families must commit to helping underserved youth succeed. Thus, YDIP requires parents to take an active role in their students’ scholarship and progress.</p>
        <h2>Program Details</h2>
        <h3>Our philosophy</h3>
        <p>At YDIP, we believe families have the capacity to make progress and improve their socioeconomic status through education. YDIP prepares youth for college by offering extensive educational and social support and facilitating participation from students and parents. YDIP also communicates regularly with teachers, administrators, and guidance counselors at the students’ schools, to solve problems if they arise and ensure that YDIP students succeed.</p>
        <p>Students are accepted into YDIP in middle school and remain in the program through high school.</p>
        <p>YDIP offers the following services:</p>
        <ul>
            <li>After-school tutoring</li>
            <li>Saturday study program</li>
            <li>Professional development workshops</li>
            <li>Mentoring</li>
            <li>Health and wellness guidance</li>
            <li>Support from teachers, parents, and schools</li>
            <li>Family financial literacy and budgeting workshops</li>
            <li>Summer enrichment and employment program</li>
        </ul>
        <p>Our services are free to parent and student participants.</p>
        <h3>Our services</h3>
        <h4>Tutoring</h4>
        <p>Northeastern students tutor YDIP youth Monday through Thursday, 3–7 p.m. YDIP staff are on-site to supervise the tutors and students and ensure that the sessions are productive. YDIP students are required to spend at least four hours a week working on schoolwork.</p>
        <h4>Saturday program</h4>
        <p>YDIP requires students to attend a full-day Saturday program twice a month. The program includes classes in English and math that are aligned with Boston Public Schools curricula, as well as a life skills class about subjects such as effective decision-making and how to develop resilience. We also take trips to visit colleges and universities in and outside Boston, attend cultural events on- and off-campus, and go on fun field trips to reward YDIP students for their hard work in the classroom.</p>
        <h4>Summer program</h4>
        <p>YDIP requires students and parents to participate in a summer program from 8 a.m. to 3 p.m. five days a week for six weeks. The program combines career exposure, work experience, and academics. Students attend classes in subjects such as math, history, and English; work in paid internships at Northeastern; and participate in recreational activities like swimming, volleyball, and basketball. We provide students with breakfast and lunch.</p>
        <p>For the internships, we place work-eligible students in Northeastern departments that align with their career interests. These sites include the Graduate School of Engineering, School of Nursing, Snell Library, and Curry Student Center.</p>
        <p>Parents are required to attend weekly meetings and read a summer reading book assigned by YDIP staff. Each week, parents discuss the book and how it relates to their students and their own lives. Meetings are held in the evening to accommodate parents’ schedules.</p>
        <h2>Roles and Positions</h2>
        <h3>Youth and their parents</h3>
        <p>YDIP expects students to:</p>
        <ul>
            <li>Give 100% effort on school work inside and outside the classroom</li>
            <li>Complete at least four hours of after-school tutoring each week</li>
            <li>Attend the YDIP Saturday program twice a month</li>
            <li>Ask for help when needed</li>
            <li>Complete required summer reading</li>
        </ul>
        <p>YDIP expects parents to:</p>
        <ul>
            <li>Attend YDIP workshops and other activities two Saturdays per month. These events may include discussions of student progress, workshops on financial literacy, and guidance about effective parenting for academic success.</li>
            <li>Notify YDIP staff and attend make-up meetings if they can’t attend the Saturday workshops</li>
            <li>Be involved and engaged in their child’s education at school and at YDIP</li>
            <li>Attend weekly meetings in the summer and complete required summer reading</li>
            <li>Communicate often about students’ academic progress or regress, disciplinary issues that occur in or outside the program, and family issues that may affect students’ academic performance</li>
        </ul>
        <h3>Tutors from Northeastern</h3>
        <p>YDIP tutors are Northeastern students who assist YDIP students with study skills and other learning strategies for academic success. They work 5–10 hours per week, depending on their availability and program needs. They also take part in monthly professional development sessions to learn skills they can use at YDIP and in future roles.</p>
        <p>Tutors are hired at the start of every semester.</p>
