        <h2 id="support-and-partnership">Support or Partner with Us</h2>
        <h3>Host a Fellow</h3>
        <p>Hosting a Fellow is a valuable investment because it allows organizations to enhance their capacity and offer programs that contribute to a greater community impact.</p>
        <p>Fellows enable organizations to:</p>
        <ul>
            <li>Expand the capacity of Out-of-School-Time programming for youth</li>
            <li>Improve the quality and effectiveness of current youth programming</li>
            <li>Partner with a statewide network of organizations and agencies that focus on serving young people</li>
            <li>Bring creativity and a fresh perspective to the Out-of-School-Time program</li>
            <li>Manage and support program volunteers</li>
        </ul>
        <p>MPF provides host organizations with:</p>
        <ul>
            <li>A full-time Fellow (35 hours per week) committed to 11 months of service</li>
            <li>Orientation and training for the host site and Fellow supervisor</li>
            <li>Leadership training, project management training, regular retreats, monthly meetings, and optional training opportunities for Fellows</li>
            <li>Professional development for host site supervisors</li>
            <li>Tuition discount on courses in the College of Professional Studies for host site supervisors</li>
            <li>Administration of Fellow benefits such as annual stipend and health insurance</li>
            <li>Access to the national AmeriCorps service network and associated training and conferences</li>
        </ul>
    </div>
