        <h1>{{ $page->title }}</h1>
        <p class="lead">We believe young people need five key resources to succeed in life: caring adults, a safe place, a healthy start, a quality education, and an opportunity to serve. Each year, Massachusetts Promise Fellowship trains 40 people to provide those five resources to youth across the Commonwealth. These Fellows—typically recent college graduates—work at non­profit organizations, schools, and government agencies to lead out­-of­-school-time programs for 3,000+ Massachusetts youth annually in grades 6–12. Our paid Fellows provide mentoring, college and career readiness, and academic enrichment.</p>
        <p class="lead">MPF is an AmeriCorps program hosted by Northeastern’s Office of City and Community Engagement and supported by the Massachusetts Service Alliance and the Corporation for National and Community Service.</p>
        <h2>Program Details</h2>
        <h3>Benefits</h3>
        <p>Serving as a Massachusetts Promise Fellowship is an incredible way to make a difference in the community, grow professionally, and build lifelong friends. Some of the benefits of participating in the program include:</p>
        <h4>Supporting youth</h4>
        <p>Fellows are champions for the young people they serve. Last year, Fellows supported 796 youth in academic enrichment and college and career readiness. And 350 students supported by MPF Fellows celebrated acceptances into post-secondary institutions or training programs.</p>
        <h4>Serving communities</h4>
        <p>Fellows have served in schools, government agencies, and nonprofit organizations in 50+ communities across Massachusetts. Some of these prior host sites include:</p>
        <ul>
                <li>Asian Community Development Corporation</li>
                <li>The Boston Public Health Commission, Start Strong Initiative</li>
                <li>Brockton Boys and Girls Club</li>
                <li>Freedom House</li>
                <li>Girls, Inc. of Lynn</li>
                <li>Mass General Hospital</li>
                <li>Mission Safe</li>
                <li>Regional Environmental Council, Worcester</li>
                <li>Youth Enrichment Services</li>
        </ul>
        <h4> Academic and training opportunities</h4>
        <p>Massachusetts Promise Fellows receive career coaching and more than 170 hours of training in leadership, social justice, youth development, and other subjects. See more in the Training and Leadership Development section.</p>
        <p>Fellows are also eligible to take free courses at Northeastern University’s College of Professional Studies during their service year. They can choose from in-person evening classes or online courses, in undergraduate studies, graduate studies, or continuing education.</p>
        <p>From 2007–2018, 300 Fellows have taken courses at CPS. Forty-sevenFellows have received graduate certificates and 37 have received master’s degrees.</p>
        <p>Upon successful completion of a year of service with MPF, Fellows are also eligible for a 25% discount on CPS courses for up to seven years after their service.</p>
        <p>Fellows who are interested in pursuing classes while completing a year of service are required to apply to CPS for admission. For more information, visit the <a href="https://cps.northeastern.edu/">CPS website</a>.</p>
        <h4>Career advancement</h4>
        <p>MPF’s training curriculum, networking opportunities, and career coaching help Fellows build the skills to succeed in the private and public sector after their MPF service. Many  alumni continue their careers in the MPF network. For example:</p>
        <ul>
                <li>Half of all Fellows complete a second year in the program.</li>
                <li>On average, 25% of Fellows are hired by their host site for full-time employment after their year of service.</li>
                <li>18 alumni are currently employed by host sites in our 2019–2020 program.</li>
        </ul>
        <h4>Pay and financial assistance</h4>
        <p>For a year of service, Fellows receive:</p>
        <ul>
                <li>Taxable stipend of up to $15,500</li>
                <li>Segal AmeriCorps Education Award of up to $6,195</li>
                <li>$60 monthly transportation allowance (for gas or an MBTA pass)</li>
                <li>Health insurance</li>
                <li>Loan forbearance for government loans</li>
                <li>Public service loan forgiveness</li>
        </ul>
        <p>To live on the $15,500 stipend, many Fellows take part-time jobs, live with roommates, and find ways to earn extra income. Because Fellows receive a stipend and not a wage, they qualify for public assistance. Many take advantage of this benefit and receive SNAP benefits (also known as food stamps) and/or fuel assistance during the service year. MPF supports Fellows in accessing public assistance programs and benefits.</p>
        <p>The Segal AmeriCorps Education Award is a post-service benefit earned by individuals who complete an approved term of national service in an AmeriCorps program such as MPF. MPF Fellows receive this award once they successfully complete 1,700 hours in the program.</p>
        <p>The award may be used to pay for educational expenses at eligible post-secondary institutions, including many technical schools and G.I. Bill-approved programs, or to repay qualified student loans. The dollar amount of the education award is equal to the maximum amount of the U.S. Department of Education Pell Grant and may change year to year.</p>
