            <x-kernl-accordion.item title="Criteria to host a Fellow">
                <div class="prose max-w-3xl">
                    <p>Each December, MPF releases our Request for Proposals to host a Massachusetts Promise Fellow. During the proposal phase, sites are asked to share their organizational capacity, the proposed project design, and Fellow support structure. In addition to completing the proposal, accepted sites are responsible for a cost match to defray part of the cost of the fellowship position and abide by the policies outlined by AmeriCorps and the Corporation for National and Community Service.</p>
                    <p>To host a Fellow, organizations must:</p>
                    <ul>
                            <li>Have a mission that aligns with MPF’s mission</li>
                            <li>Be a school, government agency, or nonprofit organization with tax-exempt, 501(c)(3) status</li>
                            <li>Be in operation for at least one year</li>
                            <li>Have at least one full-time staff member</li>
                            <li>Have a need and a clear vision for the Fellow’s role, including realistic goals and measurable outcomes</li>
                            <li>Have the organizational capacity to support, develop, and manage a Fellow, ensuring that the Fellow is treated as an integral part of the organization</li>
                            <li>Have a designated host site supervisor who will provide weekly supervision and support to the Fellow</li>
                            <li>Reimburse the Fellow for travel costs incurred as part of their service to your organization (MPF pays for other fellowship-related travel)</li>
                    </ul>
                    <p>For questions about the hosting a Massachusetts Promise Fellow, contact Lauren Currie at <a href="mailto:l.currie@northeastern.edu">l.currie@northeastern.edu</a> or 617.373.5312.</p>
