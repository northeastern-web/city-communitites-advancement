                <div class="prose max-w-3xl">
                    <p>For a year of service, Fellows receive:</p>
                    <ul>
                            <li>Taxable stipend of up to $15,500</li>
                            <li>Segal AmeriCorps Education Award of up to $6,195</li>
                            <li>$60 monthly transportation allowance (for gas or an MBTA pass)</li>
                            <li>Health insurance</li>
                            <li>Loan forbearance for government loans</li>
                            <li>Public service loan forgiveness</li>
                    </ul>
                    <p>To live on the $15,500 stipend, many Fellows take part-time jobs, live with roommates, and find ways to earn extra income. Because Fellows receive a stipend and not a wage, they qualify for public assistance. Many take advantage of this benefit and receive SNAP benefits (also known as food stamps) and/or fuel assistance during the service year. MPF supports Fellows in accessing public assistance programs and benefits.</p>
                    <p>The Segal AmeriCorps Education Award is a post-service benefit earned by individuals who complete an approved term of national service in an AmeriCorps program such as MPF. MPF Fellows receive this award once they successfully complete 1,700 hours in the program.</p>
                    <p>The award may be used to pay for educational expenses at eligible post-secondary institutions, including many technical schools and G.I. Bill-approved programs, or to repay qualified student loans. The dollar amount of the education award is equal to the maximum amount of the U.S. Department of Education Pell Grant and may change year to year.</p>
