        <h2 id="roles-and-positions">Roles and Positions</h2>
        <p>Each year, The Massachusetts Promise Fellowship hires 40 talented, passionate and committed Fellows to serve in our program.</p>
        <p>The qualifications to serve as a Massachusetts Promise Fellow are:</p>
        <ul>
            <li>Deep belief in the power and potential of young people to create social change</li>
            <li>Experience working with youth</li>
            <li>Passion for social justice and working with diverse communities</li>
            <li>Commitment to national service</li>
            <li>Ability to work independently and as part of a team</li>
            <li>Proven leadership and project management abilities</li>
            <li>Excellent organizational and communication skills</li>
            <li>A growth mindset and sense of humor</li>
            <li>A high school degree or GED equivalent</li>
            <li>Age 17 or older</li>
            <li>Willingness to complete a criminal background check</li>
            <li>U.S. Citizen or Permanent Resident Alien</li>
        </ul>
